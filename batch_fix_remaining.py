def apply_fixes():
    """修正を適用"""
    print("""
========================================================
   スキマニT2S 残り16ファイルの要約修正
========================================================
""")

    success = 0

    for fix in fixes:
        filepath = SERIES_FOLDER / fix["filename"]

        if not filepath.exists():
            print(f"[--] NOT FOUND: {fix['filename']}")
            continue

        content = filepath.read_text(encoding='utf-8')
        original = content

        # 置換
        content = content.replace(fix["old_summary"], fix["new_summary"])

        # 修正
        if content != original:
            filepath.write_text(content, encoding='utf-8')
            print(f"[OK] {fix['filename']}")
            success += 1
        else:
            print(f"[--] {fix['filename']}")

    print()
    print(f"{'='*70}")
    print(f"OK 修正完了: {success}/{len(fixes)} ファイル")
    print(f"{'='*70}\n")

if __name__ == "__main__":
    apply_fixes()
