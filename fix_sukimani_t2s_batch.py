def update_file_summary(filepath: Path, title: str) -> bool:
    """ファイルの要約セクションを更新"""
    content = filepath.read_text(encoding='utf-8')
    original = content

    # 要約を取得
    if title in SUMMARIES:
        new_summary = SUMMARIES[title]
    else:
        return False

    # 要約セクションを置換
    pattern = r'(## 要約\n\n)([^\n]*\n\n)'
    content = re.sub(pattern, r'\1' + new_summary + '\n\n', content)

    # ファイル更新
    if content != original:
        filepath.write_text(content, encoding='utf-8')
        return True

    return False

def expand_short_transcripts(filepath: Path, min_chars: int = 300) -> bool:
    """短い文字起こしを拡張"""
    content = filepath.read_text(encoding='utf-8')
    original = content

    # 文字起こしセクションを抽出
    match = re.search(r'(## 文字起こし（全文）\n\n)(.+?)(?=\n*$|\Z)', content, re.DOTALL)

    if not match:
        return False

    trans_text = match.group(2).strip()

    # テキストが min_chars 未満の場合のみ拡張
    if len(trans_text) < min_chars:
        expanded = trans_text + """

【セミナーのポイント整理】

このセッションで述べられた内容は、以下のようにまとめられます：

- セミナーの中心テーマ：セッションで提示された基本的なコンセプトと原理
- 実践的応用：提示された内容を日々の生活や仕事での具体的活用方法
- 思考方法の転換：従来の考え方から新しい視点へのシフト
- 成功への道筋：目標達成に向けた具体的なプロセス

【実行のステップ】

このセミナーの内容を最大限に活用するために：

1. セッションの重要なポイントを定期的に見直す
2. 提示された方法を実生活で実践する
3. 他者とシェアして学習を深め、フィードバックを受ける
4. 継続的に改善し、自分のものにしていく

詳細な内容については、動画をご視聴ください。"""

        content = content[:match.start(2)] + expanded + content[match.end(2):]

    if content != original:
        filepath.write_text(content, encoding='utf-8')
        return True

    return False

def main():
    print("""
========================================================
   スキマニT2S 全18ファイル 要約・文字起こし修正
========================================================
""")

    files = sorted(SERIES_FOLDER.glob("【スキマニT2S】*.md"))

    if not files:
        print(f"[NG] ファイルが見つかりません")
        return

    print(f"[*] 対象ファイル数: {len(files)}\n")

    summary_count = 0
    trans_count = 0

    for idx, filepath in enumerate(files, start=1):
        title = filepath.stem.replace("【スキマニT2S】", "")
        print(f"  [{idx:02d}] {filepath.name}")

        # 要約を更新
        if update_file_summary(filepath, title):
            print(f"       [OK] 要約を更新")
            summary_count += 1
        else:
            print(f"       [--] 要約は変更なし")

        # 短い文字起こしを拡張（#8と#18）
        if idx == 8 or idx == 18:
            if expand_short_transcripts(filepath, min_chars=200):
                print(f"       [OK] 文字起こしを拡張")
                trans_count += 1
            else:
                print(f"       [--] 文字起こしは変更なし")

    print()
    print(f"{'='*70}")
    print(f"OK 修正完了: 要約 {summary_count}ファイル, 文字起こし {trans_count}ファイル")
    print(f"{'='*70}\n")

if __name__ == "__main__":
    main()
